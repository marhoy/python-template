
def add(a: int, b: int) -> int:
    """Adds two numbers

    Args:
        a (int): A number
        b (int): Another number

    Returns:
        The sum of the two numbers

    Example:
        >>> add(1, 2)
        3
    """
    return a + b


def sub(a: int, b: int) -> int:
    """Subtract two numbers

    Args:
        a (int): A number
        b (int: Another number

    Returns:
        The function returns a - b

    Example:
        >>> sub(2, 1)
        1
    """
    return a - b
